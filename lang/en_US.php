<h3>What Does This Do?</h3>
<p>This module helps to get a website's SEO (Search Engine Optimization) right.
Specifically, it adds several SEO capabilities and provides alerts about SEO-related
problems. Currently, the following features are supported:</p>
<ul>
<li>generate meta tags in standard html, Dublin Core, OpenGraph, Twitter and/or GooglePlus formats</li>
<li>optionally specify keywords for individual site pages</li>
<li>optionally extract keywords from site pages automatically, using specified rules and prriorities</li>
<li>provide alerts about multiple pages with the same page-titles and descriptions</li>
<li>provide alerts about short page meta descriptions</li>
<li>generate a sitemap.xml file for the site, with user-choice which pages should be indexed, and their priorities</li>
<li>automatically submit the sitemap file to searchers Google/Bing/ASK, when site content is changed</li>
<li>generate a robots.txt file explicitly disallowing access to pages you don't want to be indexed</li>
</ul>
<h3>How Do I Use It</h3>
<p>Begin by giving permissions 'Edit SEO Settings' and/or 'Edit page descriptions' to relevant groups and users</p>
<p>For each template/page which is to include metadata generated by this module:</p>
<ol>
<li>Remove the whole &lt;title&gt;&lt;/title&gt; line, and the <code>{metadata}</code> tag (even if the official advice is to never remove it) and replace them with the tag
<code>{SEOTools}</code></li>
<li>Insert at the bottom (after the closing &lt;/body&gt; element), a tag to create a block
for entering a page description, like the following:<br />
<code>{content assign='meta_description' block='yourdescblock' label='Your block label' wysiwyg=false}</code><br />
The name of the <strong>assigned variable must be recorded</strong> as one of the SEOTools module settings.
<br />
As an alternative to this content block, you can enable the module option
'Automatically generate descriptions where none is given'. This is not as good, but better than nothing.</li>
<li>After that, optionally insert another tag to create a block for entering page keywords,
like the following:<br />
<code>{content assign='meta_keywords' block='yourkeywblock' label='Your next block label' wysiwyg=false oneline=true size=70}</code><br />
Again, the <strong>assigned variable name must be recorded</strong> as one of the module settings.
Set the size to suit the expected keywords (not a lot of them - they're largely ignored anyway).<br />
If this content block is omitted, keywords will be generated at runtime from page title,
description and content, in accord with module preferences.</li>
<li>As an alternative to 1, if you add and populate the content block(s) as described in 2 and 3, you can minimally roll-your-own with something like:<br />
<code> {if empty(\$meta_description)}{capture assign='desc'}{description}{/capture}<br />
 {capture assign='meta_description'}{title|strip_tags}{if \$desc} - {\$desc}{/if}{/capture}{/if}<br />
 &lt;meta name="description" content="{\$meta_description}" /&gt;<br />
 {if !empty(\$meta_keywords)}&lt;meta name="keywords" content="{\$meta_keywords}" /&gt;{/if}</code
</li>

</ol><br />
<p>Next, visit the module's settings tabs to record page title & meta information,
sitemap & crawler settings and keyword settings for the site.<br /><br />
Under 'Page description content block identifier' and/or 'Page keywords content block identifier' you
<strong>must</strong> record the respective assignment(s) for the content-block(s)
added to site pages/templates ('meta_description' and 'meta_keywords' in the above example).<br /><br />
You should consider signing up the site with Google Webmaster tools.
Having done so, paste the Google verification code (a long string of characters and numbers)
into the box 'Verification Code' in 'SEO Settings' to automatically verify that
you are really the owner of your page (don't paste the whole Google meta-code).<br /><br />
Visit the 'Page settings' tab which lists all site pages. There, among other things, 
you can change the sitemap priority (which is auto-calculated by default) of any page
to indicate that it should be considered more important for search engines or exclude
certain pages from the search index.<br /><br />
The module will also automatically insert a link to the image to be used as the
main image of your page if you select an 'Image' for the page from within your
page's 'Options' tab (this is important for people submitting a link to your page
e.g. on facebook). Also, if you are using OpenGraph, the image selected here will
override the default OpenGraph image you specified from within 'SEO Settings'.
Please be aware that you need to upload those images to your /uploads/images directory.</p>
<p>Next, on each relevant website page<br />
<ul>
<li>in the page-description content block you've added, insert a unique page description,
75 to 200 chars long, one or two sentences. The description may include smarty tag(s).</li>
<li>in the page-keywords content block (if you've added one), insert relevant keywords/phrases,
using the separator specified in the module preferences</li>
</ul></p>
<p>Last, visit the module's 'Alerts' and 'Fixes' tabs and deal with any problems detected.
Or if you're really sure about ignoring any such problem(s), tag it/them as such.</p>
<h3>Can I use smarty variables?</h3>
<p>You can use all smarty variables and UDTs within the settings fields
<em>Page title</em>, <em>Meta page title</em>, <em>Automatically generated descriptions</em>
and <em>Additional meta tags</em>.
Include <code>{debug}</code> in a page or template to see a list of all available variables.</p>
<h3>Which smarty variables are exported by this module?</h3>
<p>Several such variables are set, and may be included (after <code>{SEOTools}</code>)
in any page or template, as follows:</p>
<ul>
<li><code>{\$seo_keywords}</code> : prints a string comprised of all page-specific and default keywords
or groups of keywords, using the module's default separator</li>
<li><code>{\$default_keywords}</code> : as for <code>{\$seo_keywords}</code>, without page-specific keywords</li>
<li><code>{\$page_keywords}</code> : as for <code>{\$seo_keywords}</code>, without default keywords</li>
<li><code>{\$title_keywords}</code> : as for <code>{\$seo_keywords}</code>, except the separator is always a space-character</li>
</ul>
